<br><h1>Présentation globale</h1>
<br>

<p>Le BTS Services Informatiques aux Organisations est un diplôme recconu par l'état de niveau BAC+2.</p>
<p>Il remplace depuis Septembre 2011, l'ancien BTS IG (Informatique de gestion).</p>
<p>En effet, cette nouvelle formule réponds aux attentes de la profession en matière de qualification.</p>
<p>Le programme a évolué en fonction de la place et du rôle des TIC (Technologies de l'Information et de la Communication)</p>
<p>Et propose deux spécialités bien distinctes : Le BTS SIO Option SISR et le BTS SIO Option SLAM.</p>



<br><h1>Les options du BTS SIO</h1>


<br>
<p>Option SISR</p>
<p>L’option Solutions d’infrastructure, systèmes et réseaux forme des professionnels des réseaux et équipements informatiques (installation, maintenance, sécurité).</p>
<p>En sortant d’un BTS SIO SISR, vous serez capables de gérer et d’administrer le réseau d’une société et d’assurer sa sécurité et sa maintenance.</p>

<p>Option SLAM</p>
<p>L’option Solutions logicielles et applications métiers forme des spécialistes des logiciels (rédaction d’un cahier des charges, formulation des besoins et spécifications, développement, intégration au sein de la société).</p>

