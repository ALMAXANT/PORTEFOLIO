<br><h1>Qu'es que la blockchain ?</h1>

<br>
<p>La blockchain est une technologie de stockage et de transmission d’informations, transparente, sécurisée, et fonctionnant sans organe central de contrôle (définition de Blockchain France).</p>
<p>Elle est la technologie au coeur  du Web Decentralisé et de son corollaire, la finance décentralisée.</p>
<p>Par extension, une blockchain constitue une base de données qui contient l’historique de tous les échanges effectués entre ses utilisateurs depuis sa création.</p>
<p>Cette base de données est sécurisée et distribuée : elle est partagée par ses différents utilisateurs, sans intermédiaire, ce qui permet à chacun de vérifier la validité de la chaîne.</p>
<p>La première blockchain est apparue en 2008 avec la monnaie numérique bitcoin, développée par un inconnu se présentant sous le pseudonyme Satoshi Nakamoto. Elle en est l’architecture sous-jacente.</p>
<p>Si blockchain et bitcoin ont été construits ensemble, aujourd’hui de nombreux acteurs (entreprises, gouvernements, etc) envisagent l’utilisation de la technologie blockchain pour d’autres cas que la monnaie numérique.</p>

<br><h1>Comment ça marche ?</h1>

<br>
<p>Toute blockchain publique fonctionne nécessairement avec une monnaie ou un token (jeton) programmable. Bitcoin est un exemple de monnaie programmable.</p>
<p>Les transactions effectuées entre les utilisateurs du réseau sont regroupées par blocs. </p>
<p>Chaque bloc est validé par les noeuds du réseau appelés les “mineurs”, selon des techniques qui dépendent du type de blockchain.</p>
<p>Dans la blockchain du bitcoin cette technique est appelée le “Proof-of-Work”, preuve de travail, et consiste en la résolution de problèmes algorithmiques.</p>

